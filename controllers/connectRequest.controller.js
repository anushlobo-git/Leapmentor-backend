/**
 * @fileoverview Connection Request Controller
 * @description Thin request/response handlers for creating, managing, canceling,
 * and transferring mentorship engagement connection requests via parameter dependency injection.
 */

const catchAsync = require("../utils/catchAsync");

const createConnectRequestController = ({ connectRequestService }) => {
  /**
   * Dispatch a new mentorship engagement connection request to a mentor.
   * @route   POST /api/v1/connect-requests
   * @access  Private (User)
   */
  const sendConnectRequest = catchAsync(async (req, res, next) => {
    const request = await connectRequestService.sendConnectRequestService(
      req.user._id,
      req.body,
      req.user,
    );
    return res.status(201).json({
      success: true,
      message: "Connect request sent successfully",
      request,
    });
  });


  /**
   * Retrieve a list of historical requests generated by the authenticated mentee.
   * @route   GET /api/v1/connect-requests/my-requests
   * @access  Private (User)
   */
  const getMyRequests = catchAsync(async (req, res, next) => {
    const requests = await connectRequestService.getMyRequestsService(
      req.user._id,
    );
    return res.status(200).json({ success: true, requests });
  });

  /**
   * Retrieve incoming requests matching specified status flags for the authenticated mentor.
   * @route   GET /api/v1/connect-requests/incoming
   * @access  Private (Mentor)
   */
  const getIncomingRequests = catchAsync(async (req, res, next) => {
    const requests = await connectRequestService.getIncomingRequestsService(
      req.user._id,
      req.query.status,
    );
    return res.status(200).json({ success: true, requests });
  });

  /**
   * Accept or reject an incoming mentorship connection request.
   * @route   PATCH /api/v1/connect-requests/:id/respond
   * @access  Private (Mentor)
   */
  const respondToRequest = catchAsync(async (req, res, next) => {
    const request = await connectRequestService.respondToRequestService(
      req.params.id,
      req.user._id,
      req.body,
    );

    return res.status(200).json({
      success: true,
      message: `Request ${req.body.status} successfully`,
      request,
    });
  });

  /**
   * Cancel a pending connection request initiated by the authenticated user.
   * @route   POST /api/v1/connect-requests/:id/cancel
   * @access  Private (User)
   */
  const cancelRequest = catchAsync(async (req, res, next) => {
    await connectRequestService.cancelRequestService(
      req.params.id,
      req.user._id,
    );

    return res.status(200).json({
      success: true,
      message: "Request cancelled successfully",
    });
  });

  /**
   * Refer a connection request to an alternative platform mentor.
   * @route   POST /api/v1/connect-requests/:id/refer
   * @access  Private (Mentor)
   */
  const referRequest = catchAsync(async (req, res, next) => {
    const result = await connectRequestService.referRequestService(
      req.params.id,
      req.user._id,
      req.body,
    );

    return res.status(200).json({
      success: true,
      message: "Request referred successfully",
      ...result,
    });
  });

  /**
   * Fetch ongoing or successfully finalized connections associated with the authenticated user.
   * @route   GET /api/v1/connect-requests/ongoing
   * @access  Private (User)
   */
  const getOngoingConnects = catchAsync(async (req, res, next) => {
    const connects = await connectRequestService.getOngoingConnectsService(
      req.user._id,
    );
    return res.status(200).json({ success: true, connects });
  });

  /**
   * Fetch detailed informational mappings linked to a specific connection context ID.
   * @route   GET /api/v1/connect-requests/:id
   * @access  Private (User)
   */
  const getConnectDetail = catchAsync(async (req, res, next) => {
    const connect = await connectRequestService.getConnectDetailService(
      req.params.id,
      req.user._id,
    );
    return res.status(200).json({ success: true, connect });
  });

  return {
    sendConnectRequest,
    getMyRequests,
    getIncomingRequests,
    respondToRequest,
    cancelRequest,
    referRequest,
    getOngoingConnects,
    getConnectDetail,
  };
};

module.exports = createConnectRequestController;
